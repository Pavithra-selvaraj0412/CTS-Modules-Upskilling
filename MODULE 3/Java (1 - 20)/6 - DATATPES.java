public class DataTypes {
    public static void main(String[] args) {
        int a = 10;
        float b = 5.5f;
        double c = 20.99;
        char d = 'A';
        boolean e = true;
        String f = "Hello World";
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }
}
